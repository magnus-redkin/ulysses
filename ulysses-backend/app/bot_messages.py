def get_message(key: str, default: str = None, **kwargs) -> str:
    """Получить сообщение по ключу с подстановкой параметров и безопасным фолбэком."""
    msg = MESSAGES.get(key)

    # Если ключа нет в основном словаре, используем переданный default
    if not msg:
        msg = default if default else MESSAGES["error_unknown"]

    if kwargs and msg:
        try:
            msg = msg.format(**kwargs)
        except Exception:
            pass

    return msg
