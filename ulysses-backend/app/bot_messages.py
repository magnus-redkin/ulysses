def get_message(key: str, **kwargs) -> str:
    """Получить сообщение по ключу с подстановкой параметров."""
    msg = MESSAGES.get(key, MESSAGES["error_unknown"])
    if kwargs:
        msg = msg.format(**kwargs)
    return msg
